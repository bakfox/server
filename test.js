class Dice {
  constructor() {
    this._luck = 0;
    this._str = 0;
    this._dex = 0;
  }
  random() {
    function randomTemp(value) {
      let index = 0; //반복 확인 값
      while (value > 0) {
        let temp = parseInt(Math.random() * value);
        switch (index) {
          case 0: //럭 증가
            this._luck += temp;
            value -= temp;
            break;
          case 1: // 힘 증가
            this._str += temp;
            value -= temp;
            break;
          case 2: // 덱 증가
            this._dex += value;
            value -= value;
            break;
        }
        index++;
      }
    }
    this._luck = 0;
    this._str = 0;
    this._dex = 0;

    let statsPoint = 0; //스텟 기본값

    if (randomMaker(10)) {
      statsPoint += 15;
      randomTemp.call(this, statsPoint);
    } else {
      statsPoint += 10;
      randomTemp.call(this, statsPoint);
    }
  }
}

